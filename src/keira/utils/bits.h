// BIT LOGIC:  ////////////////////////////////////////////////////////////////////////////////////////
#define GET_BIT(data, bit_no) ((data) >> (bit_no)) & 0b1
#define SET_BIT(data, bit_no, bit_value) \
    ((data) = ((bit_value) & 0b1) ? ((data) | (1 << (bit_no))) : ((data) & ~(1 << (bit_no))))
